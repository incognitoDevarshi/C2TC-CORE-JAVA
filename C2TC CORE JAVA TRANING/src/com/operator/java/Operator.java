package com.operator.java;

public class Operator {
	
	public static void main(String[] args) {
	
		int x = 4;
		
		x += 5; // x = x + 5    = 9
		
		System.out.println(x);
		
		x -= 5; // x = x - 5     = 9-5 = 4
		
		System.out.println(x);
		
		x *= 5; // x = x * 5     = 4*5=20
		
		System.out.println(x);
		
		x /= 4; // x = x / 4     =  20/4 = 5
		
		System.out.println(x);
		
		
		x %= 5;   // x = x % 5   = 5%5  = 0
		
		System.out.println(x);
	
	}

}
